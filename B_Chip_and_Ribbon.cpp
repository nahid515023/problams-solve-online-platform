#include <bits/stdc++.h>
using namespace std;
#define int long long

void solve()
{
    int n;
    cin >> n;
    vector<int> v(n);
    for (int &x : v)
        cin >> x;

    int ans = v[0] - 1;
    for (int i = 1; i < n; i++)
    {
        if (v[i] > v[i - 1])
        {
            ans += (v[i] - v[i - 1]);
        }
    }
    cout << ans << endl;
}

